class Components_Runner_Snapshot
{
    /**
     * The configuration for the current job.
     *
     * @var Components_Config
     */
    private $_config;

    /**
     * The factory for PEAR dependencies.
     *
     * @var Components_Pear_Factory
     */
    private $_factory;

    /**
     * The output handler.
     *
     * @param Component_Output
     */
    private $_output;

    /**
     * Constructor.
     *
     * @param Components_Config       $config  The configuration for the current
     *                                         job.
     * @param Components_Pear_Factory $factory The factory for PEAR
     *                                         dependencies.
     * @param Component_Output        $output  The output handler.
     */
    public function __construct(
        Components_Config $config,
        Components_Pear_Factory $factory,
        Components_Output $output
    ) {
        $this->_config = $config;
        $this->_factory = $factory;
        $this->_output = $output;
    }

    public function run()
    {
        $options = $this->_config->getOptions();
        if (!empty($options['destination'])) {
            $archivedir = $options['destination'];
        } else {
            $archivedir = getcwd();
        }
        $options['logger'] = $this->_output;
        $result = $this->_config->getComponent()->placeArchive(
            $archivedir, $options
        );
        if (isset($result[2])) {
            $this->_output->pear($result[2]);
        }
        if (!empty($result[1])) {
            $this->_output->fail(
                'Generating snapshot failed with:'. "\n\n" . join("\n", $result[1])
            );
        } else {
            $this->_output->ok('Generated snapshot ' . $result[0]);
        }
    }
}
